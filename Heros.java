
public class Heros {

private	String nomHeros;
private	int vieHeros;
private	int forceHeros;
private	int armureHeros;
private	int bourseHeros;
	
	public Heros (){
		this.vieHeros=100;
		this.forceHeros=5;
		this.armureHeros=10;
		this.bourseHeros=0;
		
	}
	public String getNomHeros() {
		return nomHeros;
	}

	public void setNomHeros(String nomHeros) {
		this.nomHeros = nomHeros;
	}

	public int getVieHeros() {
		return vieHeros;
	}

	public void setVieHeros(int vieHeros) {
		this.vieHeros = vieHeros;
	}

	public int getForceHeros() {
		return forceHeros;
	}

	public void setForceHeros(int forceHeros) {
		this.forceHeros = forceHeros;
	}

	public int getArmureHeros() {
		return armureHeros;
	}

	public void setArmureHeros(int armureHeros) {
		this.armureHeros = armureHeros;
	}

	public int getBourseHeros() {
		return bourseHeros;
	}

	public void setBourseHeros(int bourseHeros) {
		this.bourseHeros = bourseHeros;
	}

}
